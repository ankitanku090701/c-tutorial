#include <stdio.h>

int main()
{
    // Create variables
    int a = 5;            // Integer (whole number)
    float b = 10.99;      // Floating point number
    char c = 'D';         // Character
    double d = 10.999999; // double floating point number

    // Print variables
    printf("%d\n", a);
    printf("%f\n", b);
    printf("%c\n", c);
    printf("%lf\n", d);
    return 0;
}